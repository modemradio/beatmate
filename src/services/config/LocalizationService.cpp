LocalizationService::LocalizationService() {
    // Pull the persisted choice so we boot in the correct language instead
    currentLang_ = readSavedLanguageCode();
    loadTranslations(currentLang_);
    spdlog::info("LocalizationService: boot language = {}", currentLang_);
}

void LocalizationService::setLanguage(const std::string& code) {
    bool actuallyChanged = false;
    {
        std::lock_guard<std::mutex> g(lock_);
        if (code != currentLang_) {
            currentLang_ = code;
            actuallyChanged = true;
        }
    }
    if (actuallyChanged) {
        loadTranslations(code);
        spdlog::info("LocalizationService: language -> {}", code);
    }
    // Always broadcast. Listeners like SettingsView/ContentComponent may
    sendChangeMessage();
}

std::string LocalizationService::tr(const std::string& key) const {
    std::lock_guard<std::mutex> g(lock_);
    auto it = translations_.find(key);
    return (it != translations_.end()) ? it->second : key;
}

std::vector<std::string> LocalizationService::getAvailableLanguages() const {
    return {"fr", "en"};
}

void LocalizationService::loadTranslations(const std::string& langCode) {
    const auto& dict = dictionary();
    auto it = dict.find(langCode);
    if (it == dict.end()) it = dict.find("en");
    std::lock_guard<std::mutex> g(lock_);
    translations_ = it->second;
}

