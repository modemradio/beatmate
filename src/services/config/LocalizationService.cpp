// Load the saved language from %APPDATA%/BeatMate/appsettings.json so that
std::string readSavedLanguageCode() {
    auto settingsFile = juce::File::getSpecialLocation(
            juce::File::userApplicationDataDirectory)
        .getChildFile("BeatMate")
        .getChildFile("appsettings.json");
    if (!settingsFile.existsAsFile()) return "fr";
    auto parsed = juce::JSON::parse(settingsFile.loadFileAsString());
    if (!parsed.isObject()) return "fr";
    auto* root = parsed.getDynamicObject();
    if (!root) return "fr";
    auto general = root->getProperty("general");
    if (auto* g = general.getDynamicObject()) {
        int id = (int)g->getProperty("language");
        if (id == 2) return "en";
        if (id == 1) return "fr";
    }
    return "fr";
}
